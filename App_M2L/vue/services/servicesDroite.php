				<a class="anchor" id="internet"></a>	
				<h3 class="titre">Accès Internet</h3>
				<p class="corps">Les ligues disposent d'un accès Internet mutualisé que la M2L loue à un prestataire extérieur.</p>
			</div>
			<div class="article">
				<a class="anchor" id="wifi"></a>	
				<h3  class="titre">Accès Wifi</h3>
				<p class="corps">Dans tous les espaces, un réseau Wifi "visiteurs" est disponible, avec une clé WPA renouvelée régulièrement et communiquée aux ligues. Ce réseau ne permet que l'accès à Internet.</p>
			</div>
			<div class="article">
				<a class="anchor" id="telephonie"></a>	
				<h3  class="titre">Téléphonie</h3>
				<p class="corps">Dans les bâtiments anciens A et B, les salles et bureaux sont équipés de prises de téléphone analogiques. La M2L y fournit les combinés téléphoniques. Dans les bâtiments neufs C et D, l'équipement téléphonique est de type VoIP. La M2L loue des postes téléphoniques IP aux ligues.</p>
			</div>
			<div class="article">
				<a class="anchor" id="affranchissement"></a>	
				<h3 class="titre">Affranchissement</h3>
				<p class="corps">Une machine à affranchir permet un affranchissement rapide et en nombre. Cette prestation est facturée aux ligues au coût de l'affranchissement. Chaque mois, on relie la machine à affranchir à une imprimante pour obtenir une liste de codes de gestion correspondant aux ligues associés à une quantité et un type d'affranchissement. La prise en compte de ces informations permet au CROSL d'éditer des factures.</p>
			</div>
			<div class="article">
				<a class="anchor" id="impression"></a>	
				<h3 class="titre">Impression en volume et en qualité imprimerie</h3>
				<p class="corps">Les ligues disposent de la possibilité d'imprimer sur des ressources d'impression numériques connectées situées dans le local reprographie du rez-de-chaussée dont l'usage fait l'objet d'une facturation à prix coûtant. Un système de comptage situé sur le serveur d'impression permet au CROSL d’effectuer une facturation mensuelle auprès des ligues.</p>
			</div>
			<div class="article">
				<a class="anchor" id="ftp"></a>	
				<h3 class="titre">Serveur FTP documentaire</h3>
				<p class="corps">La M2L met à disposition des ligues un serveur FTP documentaire intranet/internet regroupant des textes légaux, des modèles de dossiers, de statuts, des programmes de formation (...) compilés par le CROSL.</p>
			</div>
			<div class="article">
				<a class="anchor" id="reservation"></a>	
				<h3 class="titre">Système de réservation des salles</h3>
				<p class="corps">La M2L met à disposition des ligues un système de réservation des salles (réunions, amphithéâtre, restauration). Les réservations payantes sont facturées par la Région aux utilisateurs. L’administration de la M2L lui communique les informations nécessaires à cette facturation de façon hebdomadaire.</p>
			</div>
			<div class="article">
				<a class="anchor" id="digicode"></a>	
				<h3 class="titre">Information sur le digicode et la clé Wifi du jours</h3>
				<p class="corps">La M2L met à disposition des ligues un site web d'information sur le digicode permettant l'accès à la M2L ainsi que sur la clé Wifi "visiteurs". Le système de réservation donne également le digicode du jour dans le compte-rendu de réservation envoyé automatiquement par mail.</p>
			</div>

			<div class="article">
				<a class="anchor" id="gestion"></a>	
				<h3 class="titre">Système de gestion des configurations</h3>
				<p class="corps">M2L gère à travers un logiciel de gestion des configurations l'ensemble du parc informatique incluant les postes fixes des ligues.</p>
			</div>
			<div class="article">
				<a class="anchor" id="postes"></a>	
				<h3 class="titre">Intégration des postes informatiques des ligues</h3>
				<div class="corps">
					<p>
						Lorsque les ligues acquièrent du matériel informatique, il y a une phase obligatoire d'intégration qui consiste à :
					</p>
					<ul>
						<li>installer un antivirus affilié au serveur antiviral de la M2L,</li>
						<li>installer la dernière version de l'agent qui réalise l’inventaire matériel et logiciel</li>
						<li>paramétrer le poste en adressage IP automatique,</li>
						<li>installer un système de sauvegarde de données sur un site FTP de sauvegarde géré par la M2L,</li>
						<li>effectuer les dernières mises à jour systèmes et à paramétrer leur automatisation,</li>
						<li>paramétrer les noms des postes selon les règles de gestion suivantes :
							<ul>
								<li>B[code bâtiment], E[numéro étage], L[numéro ligue], S[numéro salle], P[numéro poste]</li>
								<li>Code bâtiment qui peut être A ou C</li>
								<li>N° étage est compris entre 1 et 4 (puisque les locaux du rez-de-chaussée n'hébergent pas de ligues)</li>
								<li>N° ligue sur 2 chiffres : correspond à un nombre attribué à la ligue allant pour l'instant de 01 à 24</li>
								<li>N° salle sur 2 chiffres : correspond aux bureaux occupés par les ligues</li>
								<li>N° poste sur 2 chiffres : correspond au numéro écrit sur la prise murale</li>
							</ul> 
						</li>
					</ul>

					<p>
						Exemple : le nom d'hôte BAE2L06S01P01 correspond au poste installé sur la prise N°1 du bureau A201 occupé par la ligue de Volley, bureau situé au deuxième étage du bâtiment A. 
					</p>
					<p>
						Cette intégration est contractualisée. Les ligues et CD étant toutes des structures associatives indépendantes, leurs postes ne sont pas intégrés dans un annuaire central. Par contre, les postes de l'administration de la M2L et de la salle multimédia le sont.
					</p>
				</div>
