		<h3 class="titre">Présentation des locaux</h3>
		<p class="corps">Le site se compose de quatre bâtiments, dont deux dotés de quatre étages et donc de 5 niveaux et deux de plain-pied dotés d’un seul rez-de-chaussée. Les bâtiments C et D sont neufs. Les bâtiments A et B datent d'une quinzaine d'années.</p>
		<p class="corps">Les étages des bâtiments A et C hébergent les bureaux des ligues locataires et tous les rez-de-chaussée héberge des espaces mutualisés : un amphithéâtre de 200 places avec une régie, 8 salles de réunion de 12 à 50 places, unhall d'accueil, une salle de convivialité et son office traiteur, ainsi qu'une salle de formation multimédia dotée de 24 postes. On y trouve également la partie « accueil » des neuf bureaux de l'administration de la M2L. L’autre partie se trouve au premier étage du bâtiment C. Différents locaux de service se trouvent en sous-sol.</p>	
